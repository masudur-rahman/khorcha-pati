function rootCatId(id: string): string {
  if (!id) return 'misc'
  const head = id.split('-')[0]
  return ICONS[head] ? head : 'misc'
}

export function categoryAccent(idOrSubcatId: string): string {
  return ACCENTS[rootCatId(idOrSubcatId)] ?? ACCENTS.misc
}

export default function CategoryIcon({ catId, size = 20 }: { catId: string; size?: number }) {
  const C = ICONS[rootCatId(catId)] ?? ICONS.misc
  return <C size={size} />
}
